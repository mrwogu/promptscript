function toolNameForFile(file: string): string {
  const normalized = file.replace(/\\/g, '/');
  if (normalized.startsWith('.github/instructions/')) return 'GitHub Copilot';
  if (normalized.startsWith('.cursor/rules/')) return 'Cursor';
  if (normalized.startsWith('.windsurf/rules/')) return 'Windsurf';
  if (normalized.startsWith('.roo/rules/')) return 'Roo Code';
  if (normalized.startsWith('.cline/rules/')) return 'Cline';
  return FILE_TOOL_NAMES[file] ?? 'Unknown';
}

