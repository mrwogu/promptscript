import { randomUUID } from 'crypto';
import { dirname, isAbsolute, relative, resolve } from 'path';
import type { CliServices } from '../services.js';

export interface PlannedWrite {
  path: string;
  content: string;
}

export interface WritePlanOptions {
  dryRun?: boolean;
  force?: boolean;
  canOverwrite?: (path: string, content: string) => boolean;
}

export interface WritePlanResult {
  created: string[];
  updated: string[];
  unchanged: string[];
}

function isManagedPromptScriptContent(content: string): boolean {
  return content
    .split(/\r?\n/)
    .slice(0, 20)
    .some(
      (line) =>
        /^<!-- PromptScript(?: [^>]*)? -->\s*$/.test(line) ||
        /^# promptscript-generated:\s+\S.*$/.test(line) ||
        /^> Auto-generated by PromptScript(?:\s.*)?$/.test(line)
    );
}

function validateWrites(writes: PlannedWrite[]): void {
  const seen = new Set<string>();
  for (const write of writes) {
    const normalized = write.path.replace(/\\/g, '/');
    const segments = normalized.split('/');
    if (
      !normalized ||
      isAbsolute(write.path) ||
      /^[A-Za-z]:/.test(normalized) ||
      normalized.startsWith('//') ||
      segments.includes('..') ||
      segments.includes('.')
    ) {
      throw new Error(`Write path must stay inside the project: ${write.path}`);
    }
    if (seen.has(normalized)) {
      throw new Error(`Write plan contains duplicate path: ${write.path}`);
    }
    seen.add(normalized);
  }
}

function isOutsideProject(projectRoot: string, candidatePath: string): boolean {
  const relativePath = relative(projectRoot, candidatePath);
  return (
    relativePath === '..' || relativePath.split(/[\\/]/)[0] === '..' || isAbsolute(relativePath)
  );
}

export async function assertSafeWritePath(path: string, services: CliServices): Promise<void> {
  if (!services.fs.realpath) return;

  const cwd = services.cwd || process.cwd();
  const projectRoot = await services.fs.realpath(cwd);
  const absolutePath = resolve(cwd, path);

  if (services.fs.lstat && services.fs.existsSync(absolutePath)) {
    const stat = await services.fs.lstat(absolutePath);
    if (stat.isSymbolicLink()) {
      throw new Error(`Refusing to replace symbolic link: ${path}`);
    }
  }

  let existingParent = dirname(absolutePath);
  while (!services.fs.existsSync(existingParent)) {
    const parent = dirname(existingParent);
    if (parent === existingParent) break;
    existingParent = parent;
  }
  const realParent = await services.fs.realpath(existingParent);
  if (isOutsideProject(projectRoot, realParent)) {
    throw new Error(`Write path escapes the project through a symbolic link: ${path}`);
  }
}

export async function executeWritePlan(
  writes: PlannedWrite[],
  services: CliServices,
  options: WritePlanOptions = {}
): Promise<WritePlanResult> {
  validateWrites(writes);
  for (const write of writes) {
    await assertSafeWritePath(write.path, services);
  }

  const originals = new Map<string, string>();
  const created: string[] = [];
  const updated: string[] = [];
  const unchanged: string[] = [];
  const conflicts: string[] = [];
  const pendingWrites: PlannedWrite[] = [];

  for (const write of writes) {
    if (!services.fs.existsSync(write.path)) {
      created.push(write.path);
      pendingWrites.push(write);
      continue;
    }

    const content = await services.fs.readFile(write.path, 'utf-8');
    originals.set(write.path, content);
    if (content === write.content) {
      unchanged.push(write.path);
      continue;
    }
    updated.push(write.path);
    pendingWrites.push(write);

    const canOverwrite =
      options.force === true ||
      isManagedPromptScriptContent(content) ||
      options.canOverwrite?.(write.path, content) === true;
    if (!canOverwrite) {
      conflicts.push(write.path);
    }
  }

  if (conflicts.length > 0) {
    throw new Error(
      `Refusing to overwrite existing files: ${conflicts.join(', ')}. Use --force or --backup.`
    );
  }

  if (options.dryRun) {
    return { created, updated, unchanged };
  }

  const tempPaths: string[] = [];
  const committed: string[] = [];

  try {
    for (const [index, write] of pendingWrites.entries()) {
      await services.fs.mkdir(dirname(write.path), { recursive: true });
      await assertSafeWritePath(write.path, services);

      if (services.fs.rename) {
        const tempPath = `${write.path}.tmp-${process.pid}-${index}-${randomUUID()}`;
        tempPaths.push(tempPath);
        await services.fs.writeFile(tempPath, write.content, { encoding: 'utf-8', flag: 'wx' });
      } else {
        committed.push(write.path);
        await services.fs.writeFile(write.path, write.content, 'utf-8');
      }
    }

    if (services.fs.rename) {
      for (const [index, write] of pendingWrites.entries()) {
        const tempPath = tempPaths[index];
        if (!tempPath) {
          throw new Error(`Missing temporary file for ${write.path}`);
        }
        await services.fs.rename(tempPath, write.path);
        committed.push(write.path);
      }
    }
  } catch (error) {
    const rollbackErrors: Error[] = [];
    for (const tempPath of tempPaths) {
      if (services.fs.rm) {
        try {
          await services.fs.rm(tempPath, { force: true });
        } catch (rollbackError) {
          rollbackErrors.push(
            new Error(
              `Could not remove temporary file ${tempPath}: ${
                rollbackError instanceof Error ? rollbackError.message : String(rollbackError)
              }`
            )
          );
        }
      }
    }

    for (const path of committed.reverse()) {
      const original = originals.get(path);
      if (original !== undefined) {
        try {
          await services.fs.writeFile(path, original, 'utf-8');
        } catch (rollbackError) {
          rollbackErrors.push(
            new Error(
              `Could not restore ${path}: ${
                rollbackError instanceof Error ? rollbackError.message : String(rollbackError)
              }`
            )
          );
        }
      } else if (services.fs.rm) {
        try {
          await services.fs.rm(path, { force: true });
        } catch (rollbackError) {
          rollbackErrors.push(
            new Error(
              `Could not remove ${path}: ${
                rollbackError instanceof Error ? rollbackError.message : String(rollbackError)
              }`
            )
          );
        }
      } else {
        rollbackErrors.push(new Error(`Could not remove ${path}: remove operation unavailable`));
      }
    }

    if (rollbackErrors.length > 0) {
      const originalError = error instanceof Error ? error : new Error(String(error));
      throw new AggregateError(
        [originalError, ...rollbackErrors],
        `${originalError.message}. Rollback failed for ${rollbackErrors.length} operation(s).`,
        { cause: error }
      );
    }
    throw error;
  }

  return { created, updated, unchanged };
}
